def artcode_i(s):
    """retourne la liste de tuples encodant une chaÃ®ne de caractÃ¨res passÃ©e en argument selon un algorithme itÃ©ratif

    Args:
        s (str): la chaÃ®ne de caractÃ¨res Ã  encoder

    Returns:
        list: la liste des tuples (caractÃ¨re, nombre d'occurences)

    >>> artcode_i('MMMMaaacXolloMM')
    [('M', 4), ('a', 3), ('c', 1), ('X', 1), ('o', 1), ('l', 2), ('o', 1), ('M', 2)]
    >>> artcode_i('gggfhhhhhhggg')
    [('g', 3), ('f', 1), ('h', 6), ('g', 3)]
    >>> artcode_i('HHHjjRReeee')
    [('H', 3), ('j', 2), ('R', 2), ('e', 4)]
    >>> artcode_i('~~~~~@@@---@@@~~~~~')
    [('~', 5), ('@', 3), ('-', 3), ('@', 3), ('~', 5)]
    """
    currentLetter = s[0]
    compteur = 1
    

    res = []
    
    for letter in s[1:]:
        if letter == currentLetter:
            compteur += 1
        else:
            res.append((currentLetter, compteur))
            currentLetter = letter
            compteur = 1
        
    res.append((currentLetter, compteur))
    
    return res

